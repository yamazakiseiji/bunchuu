/******************************************************************************
* 7SEG初期化
* 
* [概要]
* ７ＳＥＧ初期化（全消灯）
* 
******************************************************************************/
void SegInit(void)
{
	// 7SEG表示
	SegDisp(ALL_BLINK);
}

/******************************************************************************
* 7SEG表示処理
* 
* [概要]
* 7SEG表示を行う。
* 
******************************************************************************/
void SegDisp( UB num)
{
	/* 7SEG表示上限超えているか？*/
	if( num > 0x1f)
	{
		// 7SEG消灯
		*( (UB *)SEG7_ADDR ) = SEG_CLR;
	}
	else
	{
		// 7SEG数値表示
		*( (UB *)SEG7_ADDR ) = num_seg[num];
	}
}


